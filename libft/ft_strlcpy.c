#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t n)
{
	unsigned int i;
	unsigned int size;

	size = 0;
	i = 0;
	while (src[size])
		size++;
	if (n == 0)
		return (size);
	while (src[i] && i < n - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (size);
}
